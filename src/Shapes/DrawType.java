package Shapes;

/**
 * The DrawType enum defines the types of shapes that can be drawn.
 * Each enum value represents a different kind of shape.
 */
public enum DrawType {
    /**
     * Represents an ellipse shape.
     */
    ellipse,
    /**
     * Represents a line shape.
     */
    line,
    /**
     * Represents a rectangle shape.
     */
    rectangle,
    /**
     * Represents a triangle shape.
     */
    triangle,
    /**
     * Represents a diamond shape.
     */
    diamond,
}
